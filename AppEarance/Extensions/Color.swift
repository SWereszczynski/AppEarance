import UIKit

extension UIColor {
    
    static func pastelYellow() -> UIColor {
        return UIColor(red: 233/256, green: 223/256 , blue: 146/256, alpha: 1)
    }
    
    static func pastelGray() -> UIColor {
        return UIColor(red: 123/256, green: 107/256, blue: 105/256, alpha: 1)
    }
    
    static func pastelWhite() -> UIColor {
        return UIColor(red: 251/256, green: 247/256, blue: 222/256, alpha: 1)
    }
}
